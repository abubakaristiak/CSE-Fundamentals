#include <stdio.h>
int main()
{
    // Addition:

    int a = 10, b = 20;
    int sum = a + b;
    printf("%d\n", sum);

    // Substraction:

    int c = 5, d = 2;
    int sub = c - d;
    printf("%d\n", sub);

    // Multiplication:

    int e = 15, f = 3;
    int mul = e * f;
    printf("%d\n", mul);

    // Divion:

    int x = 5, y = 2;
    float div = x * 0.1 / y;
    printf("%0.2f\n", div);

    // Reminder:

    int num1 = 5, num2 = 2;
    int mod = num1 % num2;
    printf("%d\n", mod);

    return 0;
}